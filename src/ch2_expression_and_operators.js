// Addition operator (+)

let a = 10;
let b = 20;
let c = a + b;
console.log(c); // 30

// Addition assignment operator (+=)
let d = 10;
d += 20; // d = d + 20
console.log(d); // 30

// Subtraction operator (-)
let x = 20;
let y = 10;
let z = x - y;
console.log(z); // 10

// Subtraction assignment operator (-=)
let m = 20;
m -= 10; // m = m - 10
console.log(m); // 10

// Multiplication operator (*)
let p = 10;
let q = 20;
let r = p * q;
console.log(r); // 200

// Multiplication assignment operator (*=)
let s = 10;
s *= 20; // s = s * 200
console.log(s); // 200

// Division operator (/)
let l = 20;
let n = 10;
let o = l / n;
console.log(o); // 2

// Division assignment operator (/=)
let t = 20;
t /= 10; // t = t / 10
console.log(t); // 2

// Modulus operator (%)
let u = 20;
let v = 10;
let w = u % v;
console.log(w); // 0
let uu = 22;
let vv = 10;
let ww = uu % vv;
console.log(ww); // 2

// Modulus assignment operator (%=)
let aa = 20;
aa %= 10; // aa = aa % 10
console.log(aa); // 0

let bb = 11;
bb %= 10; // bb = bb % 10
console.log(bb); // 1

// Increment operator (++)
// Post increment
// a++
let cc = 10;
cc++;
console.log(cc); // 11

// Pre Increment
// ++a
let dd = 10;
++dd;
console.log(dd); // 11

// Pre and Post increment difference
let ee = 10;
let ff = ee++;
console.log("ee", ee); // 11
console.log("ff", ff); // 10

// Decrement operator (--)
// Post Decrement
// a--
// Pre Decrement
// --a
let gg = 10;
gg--;
console.log(gg); // 9

let hh = 10;
--hh;
console.log(hh); // 9

let ii = 10;
let jj = ii--;
console.log("ii", ii); // 9
console.log("jj", jj); // 10

// Exponentiation operator (**)
// 2 ** 3 = 2 * 2 * 2 = 8
// 2 ** 4 = 2 * 2 * 2 * 2 = 16
let kk = 2 ** 3;
console.log(kk); // 8

// Exponentiation assignment operator (**=)
// a **= b => a = a ** b;
let mm = 2;
mm **= 3; // mm = mm ** 3;
console.log(mm); // 8

